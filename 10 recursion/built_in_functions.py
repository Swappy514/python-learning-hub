# Built-in Functions in Python

# Example 1: Using len() to get length of a list
items = [1, 2, 3, 4, 5]
print("Length of items:", len(items))

# Example 2: Using max() and min()
numbers = [10, 20, 5, 30]
print("Max:", max(numbers))
print("Min:", min(numbers))

# Example 3: Using sum() to add all elements
print("Sum:", sum(numbers))
