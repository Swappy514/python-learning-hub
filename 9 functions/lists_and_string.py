# Lists and Strings in Python: Basic usage and interaction

# Example 1: Iterate over a string and a list
text = "hello"
chars = ['h', 'e', 'l', 'l', 'o']

# Print each character from string
for char in text:
    print(char, end=' ')
print()

# Print each element from list
for c in chars:
    print(c, end=' ')
print()
