# Relational Operators in Python
# Used to compare values, result is always a boolean (True or False)

# Examples:

a = 10
b = 20

print("a == b:", a == b)  # Equal to? False
print("a != b:", a != b)  # Not equal? True
print("a > b:", a > b)    # Greater than? False
print("a < b:", a < b)    # Less than? True
print("a >= 10:", a >= 10)  # Greater or equal? True
print("b <= 20:", b <= 20)  # Less or equal? True
