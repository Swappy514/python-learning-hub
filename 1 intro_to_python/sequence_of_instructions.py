# Sequence of Instructions in Python
# Python executes code from top to bottom (sequential execution)

# Example 1: Simple sequence of statements
x = 5       # Step 1: initialize x
y = 10      # Step 2: initialize y
total = x + y  # Step 3: compute sum
print("Total:", total)  # Step 4: display result

# Example 2: Modifying variables in sequence
counter = 0
counter = counter + 1   # counter is now 1
counter = counter + 5   # counter is now 6
print("Counter:", counter)  # Output: 6
