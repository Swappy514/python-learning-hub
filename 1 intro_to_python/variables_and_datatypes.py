# Variables and Data Types in Python

# Python variables are created when you assign a value to them.
# You do not need to declare the type explicitly.

# Example 1: Assigning variables of different types
age = 25            # integer
name = "Alex"       # string
height = 1.75       # float
is_student = True   # boolean

# Print all variables
print("Name:", name)
print("Age:", age)
print("Height:", height)
print("Student?", is_student)

# Example 2: Dynamic Typing
item = 10      # 'item' is an integer
print(item)    # Output: 10
item = "Book"  # now 'item' is a string
print(item)    # Output: Book
