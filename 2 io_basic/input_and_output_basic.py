# Input and Output Basic in Python

# Example 1: Printing output
print("Welcome to Python programming!")  # Displays text on the screen

# Example 2: Taking user input (string)
name = input("Enter your name: ")  # Waits for user to type name
print("Hello,", name)  # Greets the user

# Example 3: Taking numeric input and converting type
age = input("Enter your age: ")   # input() returns a string
age = int(age)                    # Convert input string to integer
print("You will be", age + 1, "next year!")  # Simple math and print
